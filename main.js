let lastMusicTrigger = 0; // —Å–ª–µ–¥–∏ –∫–æ–ª–∫–æ –ø—ä—Ç–∏ –µ –ø—É—Å–∫–∞–Ω–∞ –ø–µ—Å–µ–Ω—Ç–∞



const keys = {};
const player = {
    x: 0,
    y: 0,
    width: 0,
    height: 0,
    lastTimeFiredFireball: 0
};
const game = {
    speed: 2,
    movingMultiplier: 4,
    fireBallMultiplier: 5,
    fireInterval: 1000,
    cloudSpawnInterval: 3000,
    bugSpawnInterval: 1000,
    bugKillBonus: 2000,
    bonusSpawnInterval: 4000, 
    bonusPoints: 5000        
};
const scene = {
    score: 0,
    isActiveGame: true,
    lastCloudSpawn: 0,
    lastBugSpawn: 0,
    lastBonusSpawn: 0
};

